def generate_random_numbers(min_value, max_value, n):
    # Generate n random numbers from a standard Normal distribution (mean = 0, std_dev = 1)
    random_numbers = np.random.randn(n)

    # Scale and shift the numbers to fit the desired range (min_value to max_value)
    scaled_numbers = random_numbers * (max_value - min_value) / 2 + (max_value + min_value) / 2

    # Clip the values to ensure they are within the specified range
    clipped_numbers = np.clip(scaled_numbers, min_value, max_value)

    return clipped_numbers.tolist()

def count_occurrences(input_list):
    # Initialize an empty dictionary to store the results
    result_dict = {}

    # Iterate through the list and count the occurrences of each number
    for number in input_list:
        number = round(number, 2)
        if number in result_dict:
            result_dict[number] += 1
        else:
            result_dict[number] = 1

    # Sort the dictionary by keys
    sorted_dict = dict(sorted(result_dict.items()))

    return sorted_dict

